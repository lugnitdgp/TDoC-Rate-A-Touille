    private void addMenuItemBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addMenuItemBtnActionPerformed
        // TODO add your handling code here:
        //check if the list already contains the menu     the below line gets the name atv selected row && also check whether we have entered somethign on qty
        if(menuTable.getSelectedRow()!=-1 && !listModel1.contains((String)menuTable.getValueAt(menuTable.getSelectedRow(),0)) && qtyField.getText().length()!=0){
            String name=(String)menuTable.getValueAt(menuTable.getSelectedRow(), 0);//1 is row and 0 is col
            currentOrderItems.add(menuDb.findByName(name,Integer.parseInt(qtyField.getText())));//it stores the menu of the given name and id
            listModel1.addElement(name);
        }
        
    }//GEN-LAST:event_addMenuItemBtnActionPerformed

