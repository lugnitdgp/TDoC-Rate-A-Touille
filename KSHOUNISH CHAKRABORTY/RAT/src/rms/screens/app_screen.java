    private void showDetailsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showDetailsBtnActionPerformed
        // TODO add your handling code here:
       if (orderList.getSelectedIndex() != -1) {//we have selected something
            initDetailsTableModel();
            //tablemodel2.clear();
            int order = orders.get(orderList.getSelectedIndex()).getId(); //order contains the id of the selected item

            //System.out.println(order);
            for(Order ord:orders){
                if(ord.getId()==order){//check which order has been selected from the arraylist
                    for (MenuItem item : (ArrayList<MenuItem>) ord.getItems()) {

                        String row[] = {item.getName(), String.valueOf(item.getPrice()), String.valueOf(item.getQty())};
                       // System.out.println(row);
                        tableModel2.addRow(row);

                    }
                    totLbl.setText("Rs." + String.format("%5.2f", ord.getTotal())); //5 letters 2 decimals
                    System.out.println(String.format("%5.2f", ord.getTotal()));
                    break;
                }
            }
       }
    }//GEN-LAST:event_showDetailsBtnActionPerformed

    private void removeOrderBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeOrderBtnActionPerformed
        // TODO add your handling code here:
       if(orderList.getSelectedIndex()!=-1){ 
           orderDb.removeOrder(orders.get(orderList.getSelectedIndex()).getId());//removes from database
           orders.remove(orderList.getSelectedIndex());//removes from the orders 
           listModel2.remove(orderList.getSelectedIndex());//updates the table show that the details are not displayed
           
        
        }
    }//GEN-LAST:event_removeOrderBtnActionPerformed

