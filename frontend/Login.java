        if(result) {
            Popup ob=new Popup("Successfully Logged in !");
            AppScreen app=new AppScreen();
            app.setVisible(true);
            ob.setVisible(true);
            this.dispose();
        } else {
            Popup ob=new Popup("Authentication failed !");
            ob.setVisible(true);
            this.dispose();
        }
